<?php

namespace Models;

class User extends \Phalcon\Mvc\Model
{

    /**
     *
     * @var integer
     */
    public $id;

    /**
     *
     * @var string
     */
    public $name;

    /**
     *
     * @var integer
     */
    public $university_id;

    /**
     * Initialize method for model.
     */
    public function initialize()
    {
        $this->setSchema("debate");
        $this->setSource("user");
        $this->hasMany('id', 'Models\Participating', 'user_id', ['alias' => 'Participating']);
        $this->belongsTo('university_id', 'Models\University', 'id', ['alias' => 'University']);
    }

    /**
     * Returns table name mapped in the model.
     *
     * @return string
     */
